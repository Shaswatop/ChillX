// Parse riddles
const lines = raw.trim().split('\n');
const riddles = [];

for (const line of lines) {
  const parts = line.split('|');
  if (parts.length >= 3) {
    // Extract number and question
    const qMatch = parts[0].match(/^\d+\.\s*(.+)$/);
    const question = qMatch ? qMatch[1].trim() : parts[0].trim();
    const answer = parts[1].trim();
    // Clean answer (remove / variants, just use first)
    const cleanAnswer = answer.split('/')[0].trim();
    const explanation = parts.slice(2).join('|').trim();
    
    riddles.push({ question, answer: cleanAnswer, explanation });
  }
}

// Generate options for each riddle
// Use the correct answer + 3 other correct answers from the list
const allAnswers = riddles.map(r => r.answer);

const result = riddles.map((r, idx) => {
  // Get 3 wrong options from other riddles' answers
  const others = allAnswers.filter((a, i) => i !== idx && a !== r.answer);
  const shuffled = others.sort(() => Math.random() - 0.5);
  const wrongOptions = shuffled.slice(0, 3);
  
  // If we don't have enough wrong options, add some generic ones
  const genericWrong = ['धारा', 'भकारी', 'चुलो', 'परात', 'डाली', 'सिँचाई'];
  while (wrongOptions.length < 3) {
    wrongOptions.push(genericWrong[wrongOptions.length]);
  }
  
  const options = [r.answer, ...wrongOptions];
  // Shuffle options
  const shuffledOptions = options.sort(() => Math.random() - 0.5);
  
  return JSON.stringify({
    question: r.question,
    options: shuffledOptions,
    answer: r.answer,
    answer_en: r.answer,
    explanation: r.explanation,
    language: 'ne'
  });
});

const output = '[\n        ' + result.join(',\n        ') + '\n    ]';
fs.writeFileSync('riddles_output.txt', output);
